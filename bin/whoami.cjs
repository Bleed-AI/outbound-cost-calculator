#!/usr/bin/env node
/**
 * bin/whoami.cjs — identity-aware project context.
 *
 * Reads `git config user.email`, matches against `.claude/team-roster.json`,
 * and writes `.claude/local-context.md` (gitignored) with tailored guidance
 * for the current user. This file is referenced by the global ~/.claude/CLAUDE.md
 * so Claude Code reads it at session start and adapts.
 *
 * Behaviors:
 *   - OWNER (Taha): shows pending PR review queue + permission requests + recent
 *     CHANGELOG entries + reminder of one-command actions.
 *   - TEAMMATE: shows their editable scope (from CODEOWNERS), recent team changes,
 *     and how to request additional scope.
 *   - UNKNOWN: prompts to run bin/onboard.ps1 or be added to the roster.
 *
 * Wired in:
 *   - SessionStart hook (.claude/settings.json) runs this on every session open.
 *   - Can also run on demand: `node bin/whoami.cjs`.
 */

'use strict';

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const ROOT = path.resolve(__dirname, '..');
const ROSTER_PATH = path.join(ROOT, '.claude', 'team-roster.json');
const CONTEXT_PATH = path.join(ROOT, '.claude', 'local-context.md');
const PR_HUB_PATH = path.join(ROOT, 'pull-requests', 'README.md');
const CHANGELOG_PATH = path.join(ROOT, 'CHANGELOG.md');

function tryExec(cmd, fallback = '') {
  try {
    return execSync(cmd, { cwd: ROOT, encoding: 'utf8' }).trim();
  } catch {
    return fallback;
  }
}

function loadRoster() {
  if (!fs.existsSync(ROSTER_PATH)) return { owners: [], teammates: [] };
  try {
    return JSON.parse(fs.readFileSync(ROSTER_PATH, 'utf8'));
  } catch {
    return { owners: [], teammates: [] };
  }
}

function classify(email, roster) {
  const lower = (email || '').toLowerCase();
  const allEmails = (p) => {
    const list = [p.email, ...(p.aliases || [])].filter(Boolean);
    return list.map((e) => e.toLowerCase());
  };
  const owner = (roster.owners || []).find((o) => allEmails(o).includes(lower));
  if (owner) return { tier: 'OWNER', person: owner };
  const teammate = (roster.teammates || []).find((t) => !t._example && allEmails(t).includes(lower));
  if (teammate) return { tier: 'TEAMMATE', person: teammate };
  return { tier: 'UNKNOWN', person: null };
}

function countOpenPRs() {
  if (!fs.existsSync(PR_HUB_PATH)) return null; // hub not initialized yet
  const content = fs.readFileSync(PR_HUB_PATH, 'utf8');
  const matches = content.match(/^- PR #\d+/gm) || [];
  return matches.length;
}

function recentChangelogEntries(n = 5) {
  if (!fs.existsSync(CHANGELOG_PATH)) return [];
  const content = fs.readFileSync(CHANGELOG_PATH, 'utf8');
  const m = content.match(/<!-- changelog:auto-start -->([\s\S]*?)<!-- changelog:auto-end -->/);
  if (!m) return [];
  const lines = m[1]
    .split('\n')
    .filter((l) => l.startsWith('- '))
    .slice(0, n);
  return lines;
}

function ownerEditableMessage() {
  return [
    '_(All paths editable as repo owner. CODEOWNERS still gates merges of teammate PRs into PROTECTED paths.)_',
  ].join('\n');
}

function teammateEditableMessage(github) {
  // Parse CODEOWNERS to find paths where this teammate is co-owner.
  const codeownersPath = path.join(ROOT, 'CODEOWNERS');
  if (!fs.existsSync(codeownersPath)) {
    return '_(CODEOWNERS not present; standard CI applies to all paths.)_';
  }
  const content = fs.readFileSync(codeownersPath, 'utf8');
  const handle = `@${github}`;
  const protectedPaths = [];
  const grantedPaths = [];
  for (const line of content.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const [pathPattern, ...owners] = trimmed.split(/\s+/);
    if (owners.includes(handle)) {
      grantedPaths.push(pathPattern);
    } else if (owners.length > 0) {
      protectedPaths.push(pathPattern);
    }
  }
  const lines = [
    '**Paths where you can edit + auto-merge** (everything not listed below):',
    '- All paths in `clients/{slug}/`, `scripts/list-building/`, `scripts/enrichment/`, `notepad/`, etc.',
    '',
    '**PROTECTED paths** (require Taha review on PR):',
  ];
  if (protectedPaths.length === 0) lines.push('  _(none yet)_');
  else for (const p of protectedPaths.slice(0, 12)) lines.push(`  - \`${p}\``);
  if (grantedPaths.length > 0) {
    lines.push('');
    lines.push('**Paths you have explicit grant on** (you can self-approve):');
    for (const p of grantedPaths) lines.push(`  - \`${p}\``);
  }
  lines.push('');
  lines.push('To request edit access on a PROTECTED path: `node bin/request-permission.cjs --path <path> --reason "<why>"`');
  return lines.join('\n');
}

function buildContext({ tier, person }, signals) {
  const ts = new Date().toISOString().split('T')[0];
  const lines = [
    `<!-- Generated by bin/whoami.cjs on ${ts}. Gitignored — do not commit. -->`,
    '',
    `# Your project context`,
    '',
  ];

  if (tier === 'UNKNOWN') {
    lines.push(`⚠ **Email \`${signals.email || '(none)'}\` not recognized in \`.claude/team-roster.json\`.**`);
    lines.push('');
    lines.push('If you are a new teammate:');
    lines.push('1. Run `bin\\onboard.ps1` to install tools + auth.');
    lines.push('2. DM Taha to add your email + GitHub handle to `.claude/team-roster.json`.');
    lines.push('');
    lines.push('If you are Taha and the email looks wrong, fix `git config user.email` to match the entry in `team-roster.json`.');
    return lines.join('\n');
  }

  lines.push(`👋 **${person.name}** (\`${tier}\`, GitHub \`${person.github || '(not set)'}\`)`);
  lines.push('');

  if (tier === 'OWNER') {
    if (signals.openPrs === null) {
      lines.push('📭 PR review hub not yet initialized (`pull-requests/` not found). Run `node scripts/pr-review-hub.cjs` to populate.');
    } else if (signals.openPrs === 0) {
      lines.push('✅ No open PRs. Inbox zero.');
    } else {
      lines.push(`📬 **${signals.openPrs} open PR${signals.openPrs === 1 ? '' : 's'}** awaiting your review.`);
      lines.push('');
      lines.push('One-command actions:');
      lines.push('- `/review-prs` → walk through pending PRs in this Claude Code session.');
      lines.push('- `bin\\dev pr-approve N` → approve + merge PR #N.');
      lines.push('- `bin\\dev pr-reject N "<reason>"` → reject with reason.');
    }
    lines.push('');
    lines.push('## Editable scope');
    lines.push(ownerEditableMessage());
  } else {
    lines.push('## Editable scope');
    lines.push(teammateEditableMessage(person.github));
  }

  if (signals.recentChanges.length > 0) {
    lines.push('');
    lines.push('## Recent team changes (last 7 days)');
    lines.push('');
    for (const entry of signals.recentChanges) lines.push(entry);
  }

  return lines.join('\n');
}

(function main() {
  const email = tryExec('git config user.email');
  const roster = loadRoster();
  const ident = classify(email, roster);
  const signals = {
    email,
    openPrs: countOpenPRs(),
    recentChanges: recentChangelogEntries(5),
  };

  const content = buildContext(ident, signals) + '\n';
  fs.mkdirSync(path.dirname(CONTEXT_PATH), { recursive: true });
  fs.writeFileSync(CONTEXT_PATH, content);

  // Also print headline so SessionStart hook can pipe it to user.
  if (ident.tier === 'OWNER') {
    if (signals.openPrs === null) {
      console.log(`👋 ${ident.person.name} — no PR hub yet. See .claude/local-context.md`);
    } else if (signals.openPrs > 0) {
      console.log(`👋 ${ident.person.name} — 📬 ${signals.openPrs} PR${signals.openPrs === 1 ? '' : 's'} to review. See .claude/local-context.md`);
    } else {
      console.log(`👋 ${ident.person.name} — ✅ inbox zero.`);
    }
  } else if (ident.tier === 'TEAMMATE') {
    console.log(`👋 ${ident.person.name} — see .claude/local-context.md for your scope and recent changes.`);
  } else {
    console.log(`⚠ Unknown email "${email}". See .claude/local-context.md.`);
  }
})();
